    public void regresar(){
        String us = "select id_privilegios from usuario where id_usuario = '"+usuario+"'";
        int result = 0;
        try{
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(us);
            
            if(rs.next()){
                result = 1;
                if(result == 1){
                    int privilegios = rs.getInt("id_privilegios");
                    if(privilegios == 1){
                        admin form = new admin(usuario);
                        form.setVisible(true);
                        this.dispose();
                    }else if(privilegios == 2){
                        Ventas form = new Ventas(usuario);
                        form.setVisible(true);
                        this.dispose();
                    }else if(privilegios == 3){
                        bodega form = new bodega(usuario);
                        form.setVisible(true);
                        this.dispose();
                    }else if(privilegios == 4){
                        rrhh form = new rrhh(usuario);
                        form.setVisible(true);
                        this.dispose();
                    }
                }
            }else{
                System.out.println("Error: ");
            }
        }catch(Exception e){
            System.out.println("Error: " + e);
        }
    }
    
    public void crear_funcion(){
        String id_sala = salas[text_sala.getSelectedIndex()][0];
        String id_pelicula = movies[text_pelicula.getSelectedIndex()][0];
        String hora_inicio = text_hora.getSelectedItem()+":"+text_minutos.getSelectedItem();
        String hora_final = text_hora_fin.getText();
        String fecha = text_anio.getSelectedItem()+"-"+text_mes.getSelectedItem()+"-"+text_dia.getSelectedItem();
        Statement st;
        if(id_sala == null || id_pelicula == null || hora_inicio == null || hora_final == null || fecha == null){
            JOptionPane.showMessageDialog(null,"CAMPOS INCOMPLETOS");
        }else{
            String stmt = "insert into funcion values(default,'"+id_sala+"','"+id_pelicula+"','"+hora_inicio+"','"+hora_final+"','"+fecha+"')";
            try{
                st = con.createStatement();
                st.executeUpdate(stmt);
                JOptionPane.showMessageDialog(null,"FUNCION AGREGADA CON EXITO");
            }catch(Exception e){
                JOptionPane.showMessageDialog(null,"Error: "+e);
            }
        }
    }
    
    
    public void hora_fin(){
        int hora = Integer.valueOf(text_hora.getItemAt(text_hora.getSelectedIndex()));
        int minuto = Integer.valueOf(text_minutos.getItemAt(text_minutos.getSelectedIndex()));
        int duracion = Integer.valueOf(movies[text_pelicula.getSelectedIndex()][3]);
        int horas = duracion/60;
        int minutos = duracion%60;
        String minuto_text = "";
        String hora_text = "";
        hora = hora + horas;
        minuto = minuto + minutos;        
        while(minuto > 59){
            minuto = minuto - 60;
            hora = hora + 1;
        }
        if(minuto < 10){
            minuto_text = "0" + minuto;
        }else{
            minuto_text = "" +minuto;
        }
        while(hora > 23){
            hora = hora - 24;
        }
        if(hora < 10){
            hora_text = "0" + hora;
        }else{
            hora_text = "" +hora;
        }
        
        
        text_hora_fin.setText(hora_text+":"+minuto_text);
    }
    
    
    public void llenar_sala(){
        String tipo = movies[text_pelicula.getSelectedIndex()][2];
        String stmt = "select s.* from sala s inner join tipo_sala t on s.id_tipo_sala = t.id_tipo_sala WHERE t.tipo LIKE '%"+tipo+"%'";
        int result = 0;
        salas =  new String[100][2];
        text_sala.removeAllItems();
        try{
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(stmt);
            while(rs.next()){
                salas[result][0] = String.valueOf(rs.getInt("id_sala"));
                salas[result][1] = rs.getString("id_tipo_sala");
                text_sala.addItem(" ("+tipo+") SALA: "+salas[result][0]);
                result ++;
            }
           
        }catch(Exception e){
            System.out.println("Error: " + e);
        }
       
    }
